"""
 Week 1 - Numbers
 Practice based on FreeCodeCamp Python tutorial
 Concepts: math module, arithmetic operators, built-in number functions
"""

from math import floor, ceil, sqrt


# Negative float example
print(-2.098)

# Addition
print(3 + 4.5)

# Division
print(3 / 6)

# Multiplication
print(8 * 4)

# Modulus
print(10 % 3)

# Parentheses change the order of operations
print(3 * 4 + 5)
print(3 * (4 + 5))

# Converting number to string
my_num = 10
print(str(my_num) + " is my favourite colour")

# Absolute value of a number
my_num = -8
print(abs(my_num))

# Raised to the power
print(pow(3, 2))

# Returns the larger of the two numbers
print(max(6, 9))

# Returns the smallest of the two numbers
print(min(6, 9))

# Rounds number to its nearest whole number
print(round(3.7))

# Round number down
print(floor(4.8))

# Round number up
print(ceil(4.8))

# Square root
print(sqrt(36))
